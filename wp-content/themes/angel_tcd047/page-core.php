        <div class="section-content">

<img src="<?php bloginfo('template_url'); ?>/assets/images/ride/file_ride_03.jpg" alt="" class="img-responsive "/>
    <p class="mb60 pcaption">コンテストからストリート、ビッグマウンテンまでバリエーションに富んだライダーを擁するRideチーム。「さまざまなライダーがいるけど、彼らのスノーボーディングに対する取り組みかたやマインドは共通してる部分があるんだ」とチームマネージャーのタナーは語る。</p>

    <p class="mb15"><span class="question">フレッシュでいいメンツが揃ったチームだと思うんだけど、ライダーを選ぶ基準とかってあるの？</span></p>

    <p class="mb30">タナー：「量より質」ということを大事にしてる。プロになりたいから大会も撮影もなんでも頑張りますって言うのは簡単だけど、本当に大事なのは自分がなにをやりたいのか、なにが楽しいのかっていうこと。結局はうまくなること、つねに上達を目指すことなんだ。チームライダーたちはみんなそういう考え方をしていると思う。ライダーたちは撮影のとき、いつも人と違うアプローチをするよう心がけている。なぜなら、なにもかも他の人がすでにやってしまっているから。だからこそ他人とは違うことをしないとライダーとしてはこの先やっていけない。いい方向でやり続けていくには、つねにクオリティに気を配って細かいところを気にしなくてはならないしね。本当に細かいところをね。</p>

    <p class="mb15"><span class="question">そうだね。フルパートとかは特に細かい点を気にしなければいけないよね。</span></p>
    <p class="mb30">タナー：自分のやりたいことや方向性が見えている人こそが、新しいものを生み出したり、なにかを変えることができるんだ。Rideからリリースするものに関しては、Rideがなにがしたいのか、なにを信じているかを表現している。それがキッチリできていれば、観る側のスノーボーダーたちは感じてくれる。ストークしてくれたら、その作品に出てるライダーたちが乗ってる板に乗りたいって思ってくれるはずだと信じてる。</p>

    <p class="mb15"><span class="question">ジムはブランドディレクターとしてチームのことをどう考えてるの？</span></p>
    <p class="mb60">ジム：オレはタナーの感覚を信じているから、彼にかっこいいと言わせたライダーはウェルカムだよ。それに、実際にライダーに会ったり滑りを見たらわかるんだ、彼らのスノーボードに対するパッションがね。究極を言えばすべてはパッションなんだ。ジェイク・ブラウベルトはターン1発だけでめちゃくちゃヤバいだろ。ストリートをやってるライダーたちも誰がどう見てもヤバいレベルのトリックをしてる。ジャンルなんてものは関係なく、スノーボーディングが素晴らしいってことさ。そういう滑りからはものすごいパワーを感じる。みんなそうだろ？　そのパワーを感じてなかったら今日までスノーボード業界にいないと思う。たぶん今頃は株や洗濯機でも売ってるよ（笑）。</p>
