        <div class="section-content">

    <p class="mb15"><span class="question">Rideのブランドの核になるものはなんですか？</span></p>

    <p class="mb60">ジム：Rideには3つのコアがある。ひとつ目はチーム。'92年のブランド発足からRideはライダーが主体で継続してきているチームスノーボーディングブランドなんだ。ふたつ目はジョーク。Rideはこれからもジョークを飛ばしながらやっていきたい。ラフで、シリアスとは逆のスタンスだね。3つめはプロダクト。プロダクトはつねにシーンのベストでなければならない。Rideには技術力が高い本物のエンジニアたちがいて、しかも彼らは学校で技術を学んだだけではなく、リアルなスノーボーダーなんだ。ボードエンジニアのマイケルはパイプでダブルは飛んでいるし、マジで超上手い。ブーツエンジニアのトロイなんて、ストリートレールやってるからね。いまとなってはピッチピチのタイトパンツなんか履いてるけど（笑）。</p>

<img src="<?php bloginfo('template_url'); ?>/assets/images/ride/file_ride_02.jpg" alt="" class="img-responsive "/>
<p class="mb60 pcaption">（L→R）ブランドディレクターのジム・リンバーグ、マーケティングマネージャーのタナー・マッカーティ。Photo：Yoshi Josef Toomuch</p>

    <h3 class="nukcatch mb60">ものすごく頭にきたよ。それでTシャツにマジックで「Sucks（クソ）」って書いたんだ。</h3>

    <p class="mb15"><span class="question">ジムはどうやってスノーボードを始めたの？</span></p>
    <p class="mb30">ジム：'95年ごろ、Thrasher Magazineに"Cold Snap"っていうコーナーがあって、その記事のスノーボーダーの写真を見てから。「なにこれヤバいじゃん！」ってなって、すぐに父親にスノーボード買ってくれって頼み込んだよ。それで父親はウォルマートでチープなスノーボードを買ってくれたんだ。</p>
    
    <p class="mb15"><span class="question">ジムはどういった経緯で今の仕事に就いたの？</span></p>
    <p class="mb30">ジム：高校を卒業したときは、なんとしてでもスノーボードを続ける生活をしたいと思ったからモンタナ州に引っ越したんだ。そのころはK2の板を使っていて、板が壊れてしまってK2のワランティに連絡したんだけど、保証がきかなかった。ものすごく頭にきたよ。当時K2のTシャツを持っていたんだけど、それにマジックで「Sucks（クソ）」って書いたんだ。その「K2 Snowboards Sucks（K2スノーボードはクソ）」と書いたTシャツを毎日のように着てた（笑）。で、いつも通りそれを着てバーで飲んでたら、「おい、なんなんだそのTシャツは!?」って男が現れて、その男にすべての経緯を説明し終えたら、「…ええっと、オレK2の営業なんだ」って（笑）。彼が「新しい板あげたらそのTシャツ着るのやめてくれる？」って言うから、もちろんイエスと答えた（笑）。その板を貰ってからオレはK2好きに戻って、最終的には彼の下で働くことになった。その後に同じグループ会社であるRideの仕事をすることになったんだ。</p>
   
    <p class="mb15"><span class="question">タナーはどんな感じでスノーボードを始めたの？</span></p>
    <p class="mb30">タナー：家族でスキーに行くたびにいつも山でスノーボーダーを見かけていて、スキーよりもクールだからすぐ自分もスノーボードがしたいと思ってたんだ。2000年に自分の板を手にしてから、本格的にハマった。大学の頃には"East Snowboard Magazine"という雑誌を作り始めた。オレの地元のアメリカ東海岸のシーンは他と変わっている部分もあるし、たいして大きな出来事はないんだけど、東海岸で起こっているすべての出来事をその雑誌には詰め込んでたんだ。</p>
  
    <p class="mb15"><span class="question">その頃はどんなライダーたちがまわりにいた？</span></p>
    <p class="mb30">タナー：マイク・ラベルソンやジョニー・オコナーは雑誌を初めてすぐの頃にインタビューしたよ。普段つるんでる友達だからインタビューは全然うまくいかなかったけどね。マジメにやり始めても、酔っ払いすぎたりしていつもちゃんと終わらなかった（笑）。自分の少し年上の世代にはパット・ムーアがいて、彼のやっていたことはすべて真似しようとしていたよ。その後、Forumの営業の人と知り合ってオレはForumのフロウライダーになったんだ。</p>
  
    <p class="mb15"><span class="question">Eastern Boarder（多くのトップライダーを輩出する東海岸の老舗ショップ）で働いてたんだよね？　その後Rideに移ったの？</span></p>
    <p class="mb30">タナー：Eastern Boarderで働き出したことも大きな経験だった。その後大学を出るときに就職先を探していて、Rideがブランドの方向転換をするにあたってポストが空いたと聞いて応募した。Rideで起こり始めたニューウェーブに参加したかったんだ。</p>

