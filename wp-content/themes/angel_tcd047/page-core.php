        <header class="section-heading">
            <h2>BACK TO THE ROOTS</h2>
            <h3 class="mt30">そのときのオレたちの決断は「やりたいことをやること」</h3>
        </header>
        <div class="section-content">

    <p class="mb15"><span class="qustion">Rideがよりコアなイメージになっていった背景は？</span></p>

    <p class="mb30">ジム：オレがUSセールスの責任者になった頃は、売り上げとしてはRideのピークで、会社の運営をキープさせていくためにしなければいけないことも多かった。ライダーの選び方やデザインにおいてもね。Rideは歴史と信頼があるブランドのはずだった。コアなスノーボーダーたちのためにプロダクトを作り始めたのに、コアなファンたちも離れていってしまったんだ。売上も徐々にダウンし、大きな決断をする局面に立たされた。そのときのメンバーはオレとタナーとティードアとエンジニアたち。全員でこのことについて話あったよ。そのときのオレたちの決断は「やりたいことをやること」。とにかく、作りたいモノを作って、かっこよくやろうぜ。っていう話で意見がまとまったんだ。</p>

    <p class="mb15"><span class="question">それはグラフィックなどのデザインにも影響した？</span></p>
    <p class="mb60">ジム：もちろん。みんなシンプルなデザインが好きだし、カラーリングもオレたちが好きなモノクロやダークな色を使うようになっていたんだ。</p>

<img src="<?php bloginfo('template_url'); ?>/assets/images/ride/file_ride_05.jpg" alt="" class="img-responsive mb30"/>

<img src="<?php bloginfo('template_url'); ?>/assets/images/ride/file_ride_06.jpg" alt="" class="img-responsive mb60"/>

    <p class="mb15"><span class="question">板のラインナップもオールマウンテンの要素を取り入れたり、変化があったよね。フリーライドの板が増えた経緯は？</span></p>
    <p class="mb30">ジム：フリーライドボードが増えたいまのラインナップになったきっかけはジェイク・ブラウベルトがチームに入ってから。彼は人工物での滑りはかなりやってきたから、Rideに来る頃にはナチュラル地形でスノーボードがしたいという気持ちが強かったんだ。彼がシグネチャーのBerzerkerを作ったことから全てが始まったように思う。それがAlter Egoになった。その流れの後に、エンジニアのマイケルがWarpigとTimelessを作り出したんだ。</p>

    <p class="mb15"><span class="question">WarpigもTimelessもいいシェイプだよね。</span></p>
    <p class="mb30">ジム：マイケルが最初にWarpigのアイディアを持ち込んで来たときは戸惑ったよ。「こんなのアリなの？　売れんのかな？　なにコレ？」ってね（笑）。それが発売する3、4年前の話だから、マーケットにこのタイプの板は出回ってなかったしね。この誰も見たこともないようなシェイプの板が本当に売れるのか不安でしょうがなかったよ。でもマイケル本人は超本気だった。彼の熱意を感じてオレも「よっしゃ、やろうぜ。作ろう。なんとか売るよ」ってなったんだ。実際に乗ってもマジかってくらい良い板だったしね。</p>

    <p class="mb15"><span class="question">アツいね。それで、実際に人気モデルのひとつになったんだよね？</span></p>
    <p class="mb60">ジム：そう！　いまでもその人気ぶりには驚いてるよ。すべてはマイケルの熱意から始まったことがいまでは世界中の人たちに受け入れられている。しかもディレクショナルでテーパードという原点回帰したようなシェイプでね。熱い思いっていうのは人に伝染していき、やがては行動させるということを実感したよ。</p>

